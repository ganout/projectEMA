package com.wildcodeschool.projectEMA.Entity;

public class Location {
    
    private String name;

    private String url;


    public Location() {
    }


    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return this.url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}