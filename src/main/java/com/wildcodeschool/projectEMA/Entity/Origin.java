package com.wildcodeschool.projectEMA.Entity;

public class Origin {

    private String name;
    private String url;

    public Origin() {
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getUrl() {
        return this.url;
    }

    public void setUrl(String url) {
        this.url = url;
    }

}